package professor;

public class ProfessorDTO {
	String professorName;
	String professorID;
	String professorEmail;
	String professorPhone;
	String professorPassword;
	public String getProfessorName() {
		return professorName;
	}
	public void setProfessorName(String professorName) {
		this.professorName = professorName;
	}
	public String getProfessorID() {
		return professorID;
	}
	public void setProfessorID(String professorID) {
		this.professorID = professorID;
	}
	public String getProfessorEmail() {
		return professorEmail;
	}
	public void setProfessorEmail(String professorEmail) {
		this.professorEmail = professorEmail;
	}
	public String getProfessorPhone() {
		return professorPhone;
	}
	public void setProfessorPhone(String professorPhone) {
		this.professorPhone = professorPhone;
	}
	public String getProfessorPassword() {
		return professorPassword;
	}
	public void setProfessorPassword(String professorPassword) {
		this.professorPassword = professorPassword;
	}
	
}
